public class Primitives {
    public static void main (String[] args){
        System.out.println("Integer Literal: " );
        String name = "Anjani"; // String is not a primitives dat type
        int age = 19; // inter numbers
        byte age1 = 20;// byte numbers
        short num = 65; // short numbers
        long num1 = 83479834053L; // use for long numbers
        float num2 = 23.534f; // use for decimal numbers
        boolean check = true; // boolean use for true or false
        char ch = 'N'; // char use for single character
        double num3 = 234.1234; // double use for large decimal numbers
        System.out.println(age);
        System.out.println(age1);
        System.out.println(num);
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(check);
        System.out.println(ch);
        System.out.println(num3);
        System.out.println(name);



    }    
}